public class Application {

    public static void main(String[] args) {
        System.out.println("Hello World!");

        int myNumber = 100; // 32-bit value
        short myShort = 90;// 16-bit value
        long myLong = 91; //64-bit value
        double myDouble = 7.3234; //Decimal: more precision
        float myFloat = 324.5f; //Decimal: less precision
        char myChar = 'y'; //Unicode
        boolean myBool= false; //boolean
        byte myByte = 127; //8-bit

        System.out.println(myNumber);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBool);
        System.out.println(myByte);
    }

}
